#include "libft.h"

void	*ft_realloc(void *ptr, size_t size)
{
	char	*bak;

	bak = (char *)malloc(sizeof(bak) * ft_strlen(ptr));
	ft_memcpy(bak, ptr, ft_strlen(ptr));
	ptr = (char *)malloc(sizeof(char *) * size);
	ft_memcpy(ptr, bak, ft_strlen(bak));
	return (ptr);
}
