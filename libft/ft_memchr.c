#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char *t;

	t = (char *)s;
	if (n)
	{
		while (n)
		{
			if (*t == (char)c)
				return (t);
			t++;
			n--;
		}
		return (0);
	}
	else
		return (0);
}
