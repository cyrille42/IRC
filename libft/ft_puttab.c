#include "libft.h"

void	ft_puttab(const char **tab)
{
	int	i;
	int j;

	i = 0;
	j = 0;
	while (tab[j])
	{
		j++;
	}
	while (i != j)
	{
		ft_putstr(tab[i]);
		ft_putchar('\n');
		i++;
	}
}
